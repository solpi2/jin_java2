public class Main {
    public static void main(String[] args) {
        String mood = "sad";

        if(mood == "happy") {
            System.out.println("나는 행복하다.");
        } else if (mood == "angry") {
            System.out.println("나는 화가 난다");
        } else if (mood == "sad") {
            System.out.println("나는 슬프다");
        } else {
            System.out.println("나는 아무렇지 않다");
        }
    }
}